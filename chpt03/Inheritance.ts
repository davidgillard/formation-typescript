/* L'héritage est un autre paradigme qui est l'une des pierres angulaires de la programmation orientée objet.
 * L'héritage nous permet de créer un objet basé sur un autre objet, héritant ainsi de toutes ses caractéristiques,
 * y compris les propriétés et les fonctions. Dans TypeScript, l'héritage peut être utilisé par les classes et les
 * interfaces. Dans cette section du chapitre, nous examinerons de plus près l'héritage et ce qu'il signifie pour 
 * les classes et les interfaces. Lorsque nous discutons de l'héritage, il est important de faire clairement la 
 * distinction entre la classe qui constitue la base de la structure d'héritage et la classe qui effectue l'héritage.
 * Nous utiliserons le terme « classe de base » ou « interface de base » pour désigner la classe ou l'interface qui 
 * constitue la base de la structure d'héritage, et le terme « classe dérivée » ou « interface dérivée » pour désigner
 * la classe ou l'interface qui effectue l'héritage. TypeScript utilise le mot-clé extends pour implémenter l'héritage. 
 * */
